function summon(){
    var fullcost = 0;
    
    //VÖRÖSBOROK 
    var count1 = document.getElementById("count-vida").value;
    var count2 = document.getElementById("count-meszaros").value;
    var count3 = document.getElementById("count-sauskabor").value;
    var count4 = document.getElementById("count-gere").value;
    var count5 = document.getElementById("count-bonsai").value;
    var count6 = document.getElementById("count-aldas").value;

    //FEHÉRBOROK
    var count7 = document.getElementById("count-figu").value;
    var count8 = document.getElementById("count-szaszi").value;
    var count9 = document.getElementById("count-tussock").value;
    var count10 = document.getElementById("count-balla").value;
    var count11 = document.getElementById("count-laposa").value;
    var count12 = document.getElementById("count-krein").value;

    //ROSÉBOROK
    var count13 = document.getElementById("count-ballarose").value;
    var count14 = document.getElementById("count-stier").value;
    var count15 = document.getElementById("count-koch").value;
    var count16 = document.getElementById("count-lisicza").value;
    var count17 = document.getElementById("count-sauska").value;
    var count18 = document.getElementById("count-frit").value;



    //VÖRÖSBOROK
    if(document.getElementById("vida").checked){
        fullcost = fullcost + 2250*count1;
    }
    if(document.getElementById("meszaros").checked){
        fullcost = fullcost + 4190*count2;
    }
    if(document.getElementById("sauskabor").checked){
        fullcost = fullcost + 2790*count3;
    }
    if(document.getElementById("gere").checked){
        fullcost = fullcost + 4750*count4;
    }
    if(document.getElementById("bonsai").checked){
        fullcost = fullcost + 3260*count5;
    }
    if(document.getElementById("aldas").checked){
        fullcost = fullcost + 3750*count6;
    }

    //FEHÉRBOROK
    if(document.getElementById("figu").checked){
        fullcost = fullcost + 2450*count7;
    }
    if(document.getElementById("szaszi").checked){
        fullcost = fullcost + 1750*count8;
    }
    if(document.getElementById("tussock").checked){
        fullcost = fullcost + 3910*count9;
    }
    if(document.getElementById("balla").checked){
        fullcost = fullcost + 2410*count10;
    }
    if(document.getElementById("laposa").checked){
        fullcost = fullcost + 1930*count11;
    }
    if(document.getElementById("krein").checked){
        fullcost = fullcost + 2590*count12;
    }

    //ROSÉBOROK
    if(document.getElementById("ballarose").checked){
        fullcost = fullcost + 2290*count13;
    }
    if(document.getElementById("stier").checked){
        fullcost = fullcost + 1790*count14;
    }
    if(document.getElementById("koch").checked){
        fullcost = fullcost + 1820*count15;
    }
    if(document.getElementById("lisicza").checked){
        fullcost = fullcost + 1450*count16;
    }
    if(document.getElementById("sauska").checked){
        fullcost = fullcost + 2150*count17;
    }
    if(document.getElementById("frit").checked){
        fullcost = fullcost + 1980*count18;
    }

    document.getElementById("summon").innerHTML = fullcost + 1100 + " Ft.";
}

